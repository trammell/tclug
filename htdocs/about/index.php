<font size=+2>About the TCLUG</font></div>

The TCLUG was started in 1998 to bring Linux users in the Minneapolis area together. Over the past few years the LUG has grown from just a few members on a mailing list to a few thousand. One of the main purposes of the TCLUG is to provide a means for new users to get started with Linux. It also serves as a means for all users to communicate and get to know other Linux users in the area.
