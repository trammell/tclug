Join the mailing list for tons of execellent information and ideas. The latest news concerning the group is almost always to be found on the list first. Please don't
 post any job-related messages to the general list. All job posts should go to the tclug-job mailing list.
<P>
Go to the following pages to subscribe/unsubscribe:<P>
<A HREF="http://mailman.mn-linux.org/mailman/listinfo/tclug-list">TCLUG General list</A>
